public class ex09 {
   public static void main(String[] args) {
    int n = 1;
   
    System.out.println("Crescente");
    while(n <=300){
        System.out.println(n);
        n++;
    }
    System.out.println("Decrescente");
    while(n > 0){
        System.out.println(n);
        n--;
    }
   } 
}
